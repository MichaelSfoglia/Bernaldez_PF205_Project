    private void create_addButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_create_addButtonMouseClicked
        if(createproduct_nameField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "All fields are required!");
        }
        else if(duplicateCheck()){
            System.out.println("Duplicate exists");
        }
        else{
            dbConnector dbc = new dbConnector();
            if(dbc.insertData("INSERT INTO dashboard_products (product_name, product_type, product_status) VALUES ('" + createproduct_nameField.getText() + "', '" + createproduct_typeField.getText() + "', '" + createproduct_statusField.getText() + ")")){
                JOptionPane.showMessageDialog(null, "Registered Successfully!");
                
                members.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Connection Error!");
            }
        }
    }//GEN-LAST:event_create_addButtonMouseClicked

