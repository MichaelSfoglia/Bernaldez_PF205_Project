    public boolean duplicateCheck(){
        dbConnector dbc = new dbConnector();
        try{
            String query = "SELECT * FROM dashboard_product WHERE product_name " +createproduct_nameField.getText()+ "";
            ResultSet resultSet = dbc.getData(query);
            
            if(resultSet.next()){
                productname = resultSet.getString("product_name");
                if(productname.equals(createproduct_typeField.getText())){
                    JOptionPane.showMessageDialog(null, "Product's name is already used!");
                    createproduct_typeField.setText(""); 
                }
                return true;
            }
            else{
                return false;
            }
        }
        catch(SQLException ex){
            System.out.println("" +ex);
            return false;
        }
    }
    
    public boolean updateCheck(){
        dbConnector dbc = new dbConnector();
        try{
            String query = "SELECT * FROM dashboard_product WHERE member_name " +createproduct_typeField.getText() 
                    +" AND member_id != "+ createproduct_idField.getText();
            ResultSet resultSet = dbc.getData(query);
            
            if(resultSet.next()){
                if(productname.equals(createproduct_typeField.getText())){
                    JOptionPane.showMessageDialog(null, "Product's name is already used!");
                    createproduct_typeField.setText(""); 
                }
                return true;
            }
            else{
                return false;
            }
        }
        catch(SQLException ex){
            System.out.println("" +ex);
            return false;
        }
    }
